package Arrayss;

public class Arrays_Demo3 {

	public static void main(String[] args) {
		// 1D Array
		int[] a= {10,20,30};  // declaration, creation, initialiazation all done in one line
		for(int i=0;i<a.length;i++) {
			System.out.print(a[i]+" "); // if we want to print 1D column then we can simply write "ln" after "print"
		}
	}

}
