package Task_1;

public class while_even_no {

	public static void main(String[] args) {
		
		// print even no. from 50 to 65
		int i=24; // first no. is 50, thus acc to formula initialize the value 
		          // 50/2-1
		while(i<=31) // last value is 64 ---64/2-1
		{
			System.out.println(2*i+2);
			i++;
		}

	}

}
